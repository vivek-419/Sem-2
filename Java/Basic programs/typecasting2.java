class typecasting2
{
public static void main(String args[]) {
int a= 1+1;
int b= a*3;
int c= b/4;
int d= c-a;
int e= -d;
System.out.printf("a=%d,b=%d,c=%d,d=%d,e=%d",a,b,c,d,e);
System.out.println("Floating point variables");
double da=1+1;
double db=da*3;
double dc=db/4;
double dd=dc-da;
double de=c-dd;
System.out.printf("da=%f,db=%f,dc=%f,dd=%f,de=%f",da,db,dc,dd,de);

}
}
